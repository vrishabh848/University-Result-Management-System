    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        
        String rollNo=jTextField1.getText();
        String physics=jTextField2.getText();
        String maths=jTextField3.getText();
        String es=jTextField4.getText();
        String dbms=jTextField5.getText();
        String os=jTextField6.getText();
        
        
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Rishabhv@3366");
           
           Statement st=con.createStatement();
           
           ResultSet rs=st.executeQuery("select * from student where rollNo='"+rollNo+"'");
           if(rs.next()){
               st.executeUpdate("insert into result(rollNo,physics,maths,es,dbms,os) values ('"+rollNo+"','"+physics+"','"+maths+"','"+es+"','"+dbms+"','"+os+"')");
               JOptionPane.showMessageDialog(null,"result Submitted");
           }
           else{
               JOptionPane.showMessageDialog(null,"Student not registered");
           }
           setVisible(false);
           new insertResult().setVisible(true);
           
        }
        catch(Exception ee){
            JOptionPane.showMessageDialog(null, ee.toString());
        }
        
    }//GEN-LAST:event_jButton13ActionPerformed

